fun main() {
    var code: Int // Объявляем переменную ДО цикла (без инициализации)
    var input: Int // То же для ввода пользователя

    do {
        code = (1000..9999).random() // Присваиваем значение внутри

        println("Ваш код авторизации: $code")
        print("Введите код, чтобы авторизоваться: ")
        input = readln().toInt()
    } while (input != code) // Цикл повторяется, пока код неверен

    // Приветствие ЗА пределами цикла — выполняется один раз после успеха
    println("Вы авторизованы.")
}
