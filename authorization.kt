fun main() {
    // Генерируем случайные числа для капчи в диапазоне 0..10
    val num1 = (0..10).random()
    val num2 = (0..10).random()

    // Полноценное сообщение с понятной инструкцией
    print("Подтвердите, что вы не робот, сложите два числа: $num1 + $num2 = ")

    // Считываем ответ и сразу сравниваем с суммой прямо в условии
    if (readln().toIntOrNull() == num1 + num2) {
        println("Добро пожаловать!")
    } else {
        println("Доступ запрещён")
    }
}
