fun main() {

    while (true) {
        // Генерируем 4-значный код (1000..9999 включительно)
        val code = (1000..9999).random()

        println("Ваш код авторизации: $code")
        print("Введите код, чтобы авторизоваться: ")
        val input = readln().toInt()

        if (input == code) {
            println("Вы авторизованы.")
            return
        }
        // Если код неверный — цикл повторяется, генерируется новый код
    }
}
