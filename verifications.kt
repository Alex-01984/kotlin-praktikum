const val REGISTERED_NAME = "Zaphod"
const val REGISTERED_PASSWORD = "PanGalactic"

fun main() {
    print("Введите ваше имя: ")
    val name = readln()

    if (name == REGISTERED_NAME) {
        print("Введите ваш пароль: ")
        val password = readln()

        if (password == REGISTERED_PASSWORD) {
            // Точное сообщение из условия (используем тройные кавычки для удобства)
            println(
                    """Вздыхает... Ваши данные проверены, и о, чудо, они верны... 
                   Пользователь "Zaphod", вам разрешено входить на борт корабля "Heart of Gold". 
                   Хотя мне всё равно... Ну вперед, войдите... 
                   Если вам так уж надо, в конце концов... Меланхолический вздох. 
                   Надеюсь, вам понравится пребывание здесь больше, чем мне."""
            )
        } else {
            println("Неверный пароль. Доступ запрещён.")
        }
    } else {
        // Явное предложение зарегистрироваться, после чего программа завершится
        println("Пользователь не найден. Пожалуйста, пройдите регистрацию.")
    }
}
